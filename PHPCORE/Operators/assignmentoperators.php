<?php 
/*
assingment operators are used with numeric values to write a value to a variable
*/
// left = right assign
$x = 100;
$y = 200;
echo("$x $y <br>\n");

// Addition assign
$x += 10;   //$x = $x + 10;
echo("$x <br>\n");

// substract assign
$y -= $x;
echo("$y <br>\n");

//multiplication assign
$y *= 5;
echo("$y <br>\n");

//Division assign
$x /= 10;
echo("$x <br>\n");

//Modulus assign 
$y %= 7;
echo("$y <br>\n");
?>